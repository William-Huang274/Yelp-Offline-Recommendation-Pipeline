#!/usr/bin/env bash
set -euo pipefail

if [[ "${1:-}" == "-h" || "${1:-}" == "--help" ]]; then
  echo "Usage: bash scripts/runtime_sh/run_stage11_persona_sft_v3_build_dataset.sh"
  echo "Builds the Stage11 persona SFT v3 dataset. This is a data build job configured by environment variables."
  echo "Set BDA remote path variables and PERSONA_SFT_* options, then run without --help."
  exit 0
fi

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REPO_ROOT="${REPO_ROOT:-$(cd "${SCRIPT_DIR}/../.." && pwd)}"
source "${SCRIPT_DIR}/../launchers/_path_contract.sh"

PYTHON_BIN="${PYTHON_BIN:-}"
if [[ -z "${PYTHON_BIN}" ]]; then
  if command -v python3 >/dev/null 2>&1; then
    PYTHON_BIN="$(command -v python3)"
  elif command -v python >/dev/null 2>&1; then
    PYTHON_BIN="$(command -v python)"
  elif [[ -x "${BDA_REMOTE_PYTHON_BIN}" ]]; then
    PYTHON_BIN="${BDA_REMOTE_PYTHON_BIN}"
  else
    echo "[ERROR] python executable not found" >&2
    exit 1
  fi
fi

export BDA_PROJECT_ROOT="${REPO_ROOT}"
export PYTHONUNBUFFERED="${PYTHONUNBUFFERED:-1}"

export INPUT_09_USER_PREFERENCE_SCHEMA_V1_ROOT_DIR="${INPUT_09_USER_PREFERENCE_SCHEMA_V1_ROOT_DIR:-${BDA_REMOTE_PROJECT_OUTPUT_ROOT}/09_user_preference_schema_v1}"
export INPUT_09_USER_PROFILES_ROOT_DIR="${INPUT_09_USER_PROFILES_ROOT_DIR:-${BDA_REMOTE_PROJECT_OUTPUT_ROOT}/09_user_profiles}"
export INPUT_09_TIP_SIGNALS_ROOT_DIR="${INPUT_09_TIP_SIGNALS_ROOT_DIR:-${BDA_REMOTE_PROJECT_OUTPUT_ROOT}/09_tip_signals}"
export INPUT_09_SEQUENCE_VIEW_SCHEMA_V1_ROOT_DIR="${INPUT_09_SEQUENCE_VIEW_SCHEMA_V1_ROOT_DIR:-${BDA_REMOTE_PROJECT_OUTPUT_ROOT}/09_sequence_view_schema_v1}"
export INPUT_09_USER_PREFERENCE_TARGET_SCHEMA_V1_ROOT_DIR="${INPUT_09_USER_PREFERENCE_TARGET_SCHEMA_V1_ROOT_DIR:-${BDA_REMOTE_PROJECT_OUTPUT_ROOT}/09_user_preference_target_schema_v1}"
export OUTPUT_11_PERSONA_SFT_DATA_ROOT_DIR="${OUTPUT_11_PERSONA_SFT_DATA_ROOT_DIR:-${BDA_REMOTE_PROJECT_OUTPUT_ROOT}/11_persona_sft_data}"

mkdir -p "${OUTPUT_11_PERSONA_SFT_DATA_ROOT_DIR}"

export RUN_TAG="${RUN_TAG:-stage11_1_persona_sft_v3_packed_build_dataset_quality}"
export PERSONA_SFT_PROMPT_VERSION="${PERSONA_SFT_PROMPT_VERSION:-v3}"
export PERSONA_SFT_INCLUDE_DERIVED_NARRATIVE="${PERSONA_SFT_INCLUDE_DERIVED_NARRATIVE:-false}"
export PERSONA_SFT_MAX_TRAIN_ROWS="${PERSONA_SFT_MAX_TRAIN_ROWS:-0}"
export PERSONA_SFT_MAX_EVAL_ROWS="${PERSONA_SFT_MAX_EVAL_ROWS:-0}"
export PERSONA_SFT_SAMPLE_ROWS="${PERSONA_SFT_SAMPLE_ROWS:-8}"
export PERSONA_SFT_PROFILE_TEXT_MAX_CHARS="${PERSONA_SFT_PROFILE_TEXT_MAX_CHARS:-2400}"
export PERSONA_SFT_V3_MIN_TEXT_CHARS="${PERSONA_SFT_V3_MIN_TEXT_CHARS:-12}"
export PERSONA_SFT_V3_MIN_TEXT_WORDS="${PERSONA_SFT_V3_MIN_TEXT_WORDS:-2}"
export PERSONA_SFT_V3_REVIEW_MAX_ITEMS="${PERSONA_SFT_V3_REVIEW_MAX_ITEMS:-0}"
export PERSONA_SFT_V3_TIP_MAX_ITEMS="${PERSONA_SFT_V3_TIP_MAX_ITEMS:-0}"
export PERSONA_SFT_V3_INCLUDE_PROFILE_DIGEST="${PERSONA_SFT_V3_INCLUDE_PROFILE_DIGEST:-true}"
export PERSONA_SFT_V3_PROFILE_DIGEST_MIN_ITEMS="${PERSONA_SFT_V3_PROFILE_DIGEST_MIN_ITEMS:-8}"
export PERSONA_SFT_V3_APPLY_PACKING="${PERSONA_SFT_V3_APPLY_PACKING:-true}"
export PERSONA_SFT_PACK_LOW_TOTAL_CAP="${PERSONA_SFT_PACK_LOW_TOTAL_CAP:-18}"
export PERSONA_SFT_PACK_LOW_TIP_CAP="${PERSONA_SFT_PACK_LOW_TIP_CAP:-4}"
export PERSONA_SFT_PACK_LOW_NEG_FLOOR="${PERSONA_SFT_PACK_LOW_NEG_FLOOR:-2}"
export PERSONA_SFT_PACK_MID_TOTAL_CAP="${PERSONA_SFT_PACK_MID_TOTAL_CAP:-24}"
export PERSONA_SFT_PACK_MID_TIP_CAP="${PERSONA_SFT_PACK_MID_TIP_CAP:-6}"
export PERSONA_SFT_PACK_MID_NEG_FLOOR="${PERSONA_SFT_PACK_MID_NEG_FLOOR:-3}"
export PERSONA_SFT_PACK_HIGH_TOTAL_CAP="${PERSONA_SFT_PACK_HIGH_TOTAL_CAP:-36}"
export PERSONA_SFT_PACK_HIGH_TIP_CAP="${PERSONA_SFT_PACK_HIGH_TIP_CAP:-12}"
export PERSONA_SFT_PACK_HIGH_NEG_FLOOR="${PERSONA_SFT_PACK_HIGH_NEG_FLOOR:-4}"

echo "[INFO] persona SFT v3 dataset build"
echo "[INFO] repo=${REPO_ROOT}"
echo "[INFO] python=${PYTHON_BIN}"
echo "[INFO] output_root=${OUTPUT_11_PERSONA_SFT_DATA_ROOT_DIR}"
echo "[INFO] prompt_version=${PERSONA_SFT_PROMPT_VERSION}"
echo "[INFO] packing=${PERSONA_SFT_V3_APPLY_PACKING}"
echo "[INFO] pack_caps.low=${PERSONA_SFT_PACK_LOW_TOTAL_CAP}/${PERSONA_SFT_PACK_LOW_TIP_CAP}/${PERSONA_SFT_PACK_LOW_NEG_FLOOR}"
echo "[INFO] pack_caps.mid=${PERSONA_SFT_PACK_MID_TOTAL_CAP}/${PERSONA_SFT_PACK_MID_TIP_CAP}/${PERSONA_SFT_PACK_MID_NEG_FLOOR}"
echo "[INFO] pack_caps.high=${PERSONA_SFT_PACK_HIGH_TOTAL_CAP}/${PERSONA_SFT_PACK_HIGH_TIP_CAP}/${PERSONA_SFT_PACK_HIGH_NEG_FLOOR}"

"${PYTHON_BIN}" "${REPO_ROOT}/scripts/11_1_persona_sft_build_dataset.py"
